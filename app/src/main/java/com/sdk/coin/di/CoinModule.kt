package com.sdk.coin.di

import com.sdk.coin.data.remote.ApiService
import com.sdk.coin.data.repository.CoinRepositoryImpl
import com.sdk.coin.domain.repository.CoinRepository
import com.sdk.coin.utils.Constants
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import retrofit2.Retrofit
import retrofit2.converter.gson.GsonConverterFactory
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
object CoinModule {

    @Provides
    @Singleton
    fun provideRetrofit(): Retrofit {
        return Retrofit.Builder()
            .baseUrl(Constants.BASE_URL)
            .addConverterFactory(GsonConverterFactory.create())
            .build()
    }

    @Provides
    @Singleton
    fun provideApiService(retrofit: Retrofit): ApiService {
        return retrofit.create(ApiService::class.java)
    }

    @Provides
    @Singleton
    fun provideCoinRepository(apiService: ApiService): CoinRepository {
        return CoinRepositoryImpl(apiService)
    }
}